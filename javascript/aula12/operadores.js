// operador de atribuição (=)
let nota = 9.9;

// incremento (++)
let valor = 100;
valor++ // valor = valor + 1;
console.log(valor);

//decremento (--)
let valorProduto = 500;
valorProduto--; // valorProduto = valorProduto - 1;
console.log("Resultado: " + valorProduto);

// +=
let salario = 200;
salario += 10; // salario = salario + 10
console.log(salario);

// -=
let frete = 500;
frete -= 100; // frete = frete - 100;
console.log("Frete: " + frete);

// operador *=
let valorPassagem = 100;
valorPassagem *= 2; // valorPassagem = valorPassagem * 2;
console.log("Valor passagem: " + valorPassagem);

// operador /=
let soma = 400;
soma /= 10; //soma = soma / 10
console.log("Valor soma: " + soma);